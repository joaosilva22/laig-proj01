class Board {
    constructor(scene) {
	this.hex = new Hexagon(scene);
	this.board = [
	    [[' ','v',' '],[' ','v',' '],[' ','v',' '],['A','g',' '],['A','g',' '],[' ','2',' '],[' ','0',' '],[' ','v',' '],[' ','v',' ']],
	    [[' ','v',' '],[' ','v',' '],['A','g',' '],['A','g',' '],[' ','1',' '],[' ','b',' '],[' ','1',' '],[' ','2',' '],[' ','1',' ']],
	    [[' ','v',' '],[' ','v',' '],[' ','0',' '],[' ','1',' '],[' ','3',' '],[' ','z',' '],[' ','3',' '],[' ','w',' '],[' ','0',' ']],
	    [[' ','v',' '],[' ','1',' '],[' ','w',' '],[' ','2',' '],[' ','1',' '],[' ','2',' '],[' ','1',' '],[' ','1',' '],[' ','v',' ']],
	    [[' ','v',' '],[' ','3',' '],[' ','1',' '],[' ','1',' '],[' ','z',' '],[' ','1',' '],[' ','1',' '],[' ','0',' '],[' ','v',' ']],
	    [[' ','v',' '],[' ','0',' '],[' ','1',' '],[' ','1',' '],[' ','2',' '],[' ','1',' '],[' ','w',' '],[' ','3',' '],[' ','v',' ']],
	    [[' ','3',' '],[' ','b',' '],[' ','2',' '],[' ','3',' '],[' ','1',' '],[' ','1',' '],[' ','2',' '],[' ','v',' '],[' ','v',' ']],
	    [[' ','1',' '],[' ','0',' '],[' ','z',' '],[' ','w',' '],[' ','1',' '],['B','h',' '],['B','h',' '],[' ','v',' '],[' ','v',' ']],
	    [[' ','v',' '],[' ','v',' '],[' ','1',' '],[' ','0',' '],['B','h',' '],['B','h',' '],[' ','v',' '],[' ','v',' '],[' ','v',' ']],
	];
	this.scene = scene;
    }

    getMapLayer(cell) {
	return cell[1];
    }

    display() {
	if (!this.board) {
	    return;
	}

	for (let i = 0; i < this.board.length; i++) {
	    let line = this.board[i];
	    for (let j = 0; j < line.length; j++) {
		let cell = line[j];
		if (this.getMapLayer(cell) !== 'v') {
		    this.scene.pushMatrix();
		    
		    if (i % 2 === 0) {
			let offset = i / 2;
			this.scene.translate(j + offset, 0, i);
		    } else {
			let offset = (i - 1) / 2;
			this.scene.translate(j + 0.5 + offset, 0, i);
		    }
		    
		    this.hex.display();
		    this.scene.popMatrix();
		}
	    }
	}
    }

    setTexCoords() {}
}
